from datetime import datetime, timedelta
from typing import List, Optional


def pairwise(iterable):
    "s -> (s0, s1), (s2, s3), (s4, s5), ..."
    a = iter(iterable)
    return zip(a, a)


def sum_light(els: List[datetime], start_watching: Optional[datetime] = None) -> int:
    """
    how long the light bulb has been turned on
    """
    result_time = timedelta(seconds=0)

    start_watching = els[0] if not start_watching else start_watching

    for tern_on, tern_off in pairwise(els):

        if start_watching <= tern_on:
            pass
        elif tern_on < start_watching < tern_off:
            tern_on = start_watching
        elif tern_off <= start_watching:
            continue
        else:
            print('IF ERROR')

        result_time += tern_off - tern_on

    print(result_time)
    # print(result_time.total_seconds())

    return result_time.total_seconds()


